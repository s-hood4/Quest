package com.codepath.project.android.adapter;

import android.content.Context;
import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentStatePagerAdapter;

import com.codepath.project.android.fragments.ProductsListFragment;

public class ProductsFragmentPagerAdapter extends FragmentStatePagerAdapter {
    private String tabTitles[] = new String[] { "Shelf", "Wish List"};
    private Context context;

    public ProductsFragmentPagerAdapter(FragmentManager fm, Context context) {
        super(fm);
        this.context = context;
    }

    @Override
    public int getCount() {
        return tabTitles.length;
    }

    @Override
    public Fragment getItem(int position) {
        return ProductsListFragment.newInstance(position + 1);
    }

    @Override
    public CharSequence getPageTitle(int position) {
        // Generate title based on item position
        return tabTitles[position];
    }
}
